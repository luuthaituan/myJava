    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel)bang.getModel();
        int selectedRows = bang.getSelectedRow();
        try{
            id = Integer.parseInt(RecordTable.getValueAt(selectedRows, 0).toString());
            
            deleteItem = JOptionPane.showConfirmDialog(null, "Ban muon xoa sinh vien nay chu ?", "Canh bao", JOptionPane.YES_NO_OPTION);
            
            if(deleteItem==JOptionPane.YES_OPTION){
                Class.forName("com.mysql.cj.jdbc.Driver");
                sqlConn = DriverManager.getConnection(database, username, password);
                pst= sqlConn.prepareStatement("delete from danhsachsv where id =?");
                
                pst.setInt(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Da thay doi danh sach sinh vien");
                refreshDB();
                
                txtMsv.setText("");
                txtMsv.requestFocus();
                txtHoTen.setText("");
                txtLop.setText("");
                txtQueQuan.setText("");
                
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BangSV.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(BangSV.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

