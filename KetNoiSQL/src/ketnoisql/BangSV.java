    public void refreshDB() throws SQLException{
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            sqlConn = DriverManager.getConnection(database, username, password);
            pst= sqlConn.prepareStatement("select * from danhsachsv");
            
            rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            
            q=stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)bang.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next()){
                Vector columnData = new Vector();
                //them du lieu cho cac cot
                for(i=1; i<= q; i++){
                    columnData.add(rs.getInt("id")); //them truong id nhu trong csdl SQL
                    columnData.add(rs.getString("MaSoSV"));
                    columnData.add(rs.getString("HoTen"));
                    columnData.add(rs.getString("Lop"));
                    columnData.add(rs.getString("QueQuan"));
                }
                RecordTable.addRow(columnData); //du lieu cho cac hang duoc them vao
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BangSV.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    
    
